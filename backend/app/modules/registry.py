MODULES_BY_ID: dict[str, Module] = {m.id: m for m in MODULES}


def public_dict(m: Module) -> dict:
    """Публичная карточка модуля для фронта (без системного промпта)."""
    return {
        "id": m.id,
        "title": m.title,
        "short": m.short,
        "emoji": m.emoji,
        "accent": m.accent,
        "order": m.order,
        "status": m.status.value,
        "mode": m.mode.value,
        "examples": list(m.examples),
    }


def public_list() -> list[dict]:
    return [public_dict(m) for m in sorted(MODULES, key=lambda x: x.order)]
