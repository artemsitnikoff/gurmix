    status: ModuleStatus
    mode: ModuleMode
    system_prompt: str = field(repr=False)
    examples: tuple[str, ...] = ()

    @property
    def full_system_prompt(self) -> str:
        return f"{GUARDRAIL}\n\n{self.system_prompt}".strip()


