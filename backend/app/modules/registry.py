"""Реестр модулей «Нейро-шеф Гурмикс».

Источник истины для 8 модулей-экспертов. Каждый модуль — отдельная персона
с собственным системным промптом, режимом работы и состоянием (locked/active).
Перевод модуля locked -> active выполняется правкой поля `status` здесь.

Публичные поля (всё кроме `system_prompt`) отдаются фронту через
`GET /api/v1/modules`. Фронтовый `frontend/src/modules.ts` обязан зеркалить
публичные поля 1-в-1 (см. docs/CONTRACT.md).

Ключевое требование ко всем модулям: бот НЕ выдумывает. Нельзя сочинять
продукты, составы, нормы расхода, цены, дистрибьюторов и техрекомендации.
Если данных в базе нет — честно сказать и предложить оставить заявку.
"""
from __future__ import annotations

from dataclasses import dataclass, field
from enum import Enum


class ModuleStatus(str, Enum):
    ACTIVE = "active"   # модуль работает
    LOCKED = "locked"   # карточка показывается с бейджем «Скоро»


class ModuleMode(str, Enum):
    RAG = "rag"         # ответ через RAG поверх базы знаний Гурмикс
    LLM = "llm"         # ответ напрямую промптом к Opus (RAG-корпус опционален)
    TOOL = "tool"       # генерация артефакта (например, ТТК в Excel)
    DB = "db"           # ответ только из подтверждённой базы (без LLM-домысла)
    STATIC = "static"   # готовые материалы / FAQ / ссылки без AI


